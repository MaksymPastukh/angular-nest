/**
 * Интерфейс для элемента facet (фасета)
 * Представляет одно значение фильтра с количеством продуктов
 */
export interface IFacetItem {
  /** Значение фасета (например, "Nike", "Black", "M") */
  value: string;

  /** Количество продуктов с этим значением */
  count: number;
}

/**
 * Интерфейс для всех facets (фасетов) продуктов
 * Содержит информацию о доступных фильтрах и количестве продуктов для каждого значения
 */
export interface IProductFacets {
  /** Фасеты по брендам с количеством продуктов для каждого бренда */
  brands: IFacetItem[];

  /** Фасеты по типам товаров с количеством продуктов для каждого типа */
  productTypes: IFacetItem[];

  /** Фасеты по стилям одежды с количеством продуктов для каждого стиля */
  dressStyles: IFacetItem[];

  /** Фасеты по размерам с количеством продуктов для каждого размера */
  sizes: IFacetItem[];

  /** Фасеты по цветам с количеством продуктов для каждого цвета */
  colors: IFacetItem[];
}
